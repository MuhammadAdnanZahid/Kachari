            <h1 class="mb-4"> <img src="../images/icon-military.svg" alt="" style="width: 70px; height: 70px; margin-bottom: 9px;">Military Law</h1>

                   <p class="text-justify">
                    All service members in the Pakistan Armed Forces are subject to military law, consisting of the Uniform Code of Military Justice (UCMJ) and other statutory provisions. While it is similar to civilian law in many ways, such as the right to counsel and full protection under the Constitution, the UCMJ has many provisions and crimes unique to the military. These include rules against fraternization with officers; the discharge process; and the crimes of insubordination, absence without leave (AWOL), and desertion. The military tries cases through the court martial process, which is similar to a civilian trial but has its own rules and procedures. FindLaw's Military Law Center will help you navigate this sometimes confusing area of law.
                   </p>  

                   <h2>Military Criminal Law</h2>

                    <p class="text-justify">
                    When you put on the uniform of the United States military, you take on increased responsibilities as you serve your country at home or abroad. Along with these additional responsibilities comes an entire body of laws and rules called the Uniform Code of Military Justice (UCMJ). While many of these laws and rules would likely be unconstitutional if applied to everyday citizens, such as laws prohibiting insubordination or fraternization, they apply to you while you’re serving in the military, given your special position and the mission of the armed services. This section covers some of the unique laws and procedures that apply to service members as well as how the military prosecutes crimes in its own judicial forum, known as a court-martial. After reading this section, you’ll have a better understanding of military criminal law and procedures and how they can affect you and your career.</p>

                    <h2>Military Crimes</h2>

                <p class="text-justify">
                 In the civilian world, if you fail to show up for work, the worst that could happen is that you lose your job. However, in the military, this is a serious crime and can be punished with reductions in pay or rank, confinement, dishonorable discharge or, if it occurs in a combat zone, even the death penalty.
                                   </p>
                  <p class="text-justify">
                 In this section, you will learn more about criminal laws in the military:  those that are similar to laws in the civilian world, as well as those that are unique to the military. You’ll also learn about what evidence is needed to prove these crimes as well as their maximum penalties. In addition, this section explains the military’s current system of prosecuting sexual assault crimes and the resources that are available to victims.
                                   </p>
                          <h2>Military Benefits</h2>          
                                   <p class="text-justify">
                     Did you know that along with low-cost health and dental insurance, serving in the military also allows you the opportunity to obtain low-cost life insurance? Depending on the nature of your service, it can also open you up to tuition benefits for college which you may even be able to transfer to your family members. There are also significant benefits available if you face any disabilities because of your service, although in order to be eligible for such benefits, you may need to proceed through a complex medical and physical evaluation board process.
                          </p>

                            <p class="text-justify">
                       This section will cover many of the more important benefits that may be available to you and your family based on your service and will walk you through any administrative procedures that you may need to complete in order to obtain certain benefits, such as disability benefits.
                          </p>

                          <h2>Administrative Issues</h2>          
                                   <p class="text-justify">
                       The military contracting process, like most things in the military, is standardized. However, that doesn’t mean that your contract can’t be tailored in some ways to your benefit. This section will help you better understand the contracting process and what you can negotiate into your contract to help set you up for success in your military career.
                          </p>
                          <p class="text-justify">
                       This section will also explain administrative processes that can harm your career, such as non-judicial punishments and administrative separations. While these are administrative in nature and don’t have the same effect as the criminal court-martial process, they can nonetheless affect your ability to receive regular promotions and pay increases and can otherwise limit the opportunities available to you.
                          </p>
                          <p class="text-justify">
                       These administrative processes can also lead to your discharge which, if characterized as anything but honorable, can also affect your eligibility for future benefits after you separate. Thankfully, there are ways that you can challenge administrative actions, including an administrative separation, and ways to obtain a discharge upgrade even after you leave the military. This section will walk you through these critical processes to help you protect your military career and to take the steps necessary to end your career on your terms.
                          </p>

                   
                   <h2> Military Courts-Martial System</h2>
                    <p class="text-justify">
                       Often, where you face charges can be just as important as the charges themselves. Different rules and different rights apply in the military’s court-martial system. Unlike the civilian world, for example, a service member’s chain of command often has significant authority and discretion to determine whether he or she will face judicial punishment, administrative action (non-judicial punishment), or no adverse actions at all. This section will describe the different types of courts-martial along with recent legislative efforts, through the Military Justice Improvement Act, to modify the court-martial process.
                          </p>
                    <p class="text-justify">
                        Even if you don’t think you’ll face the military criminal law system during your career, it’s still important for you to know your rights as well as who can advocate on your behalf. Depending on your situation, you’ll likely have access to a free military lawyer, but you also always have the right to hire a civilian lawyer as well. There are civilian attorneys that specialize in military law who are often former military lawyers and are therefore familiar with military criminal law and procedures. These lawyers can be a significant asset to you, especially if they’re working in conjunction with a military lawyer assigned to your case.
                          </p>
                    

