                   <p class="text-justify ">Educating children is one of society's most important functions. As a result, there is a robust area of law dedicated to education. Education law is particularly fascinating because it constantly seeks to strike balances: the balance between ensuring each child receives a standard education, while maintaining a parent's right to decide what her child should learn; the balance between maintaining student safety, while respecting individual constitutional rights; the balance between accommodating students with disabilities and strict budgetary concerns; and the balance between giving teachers job security and intellectual freedom, while ensuring that they competently educate their students. This section has articles with in-depth information on education law for parents, teachers, student, and school administrators.


                   </p>

                   <h2>Choice and Compulsory Education</h2>

                    <p class="text-justify ">Education law is governed by the states. Every state requires all its children to be educated. For most students, this means that parents must send their students to an accredited public, private, or parochial (religious) school. Which school a child attends usually depends on their parents' financial means and where their primary residence is located. However, since many schools are underfunded and underperforming, many parents try to find alternative schools such as magnet or charter schools. Some parents choose to keep their children at home and take the responsibility of teaching on themselves. The section entitled "Education Options" has more information on the different ways to educate children.
                   </p>

                   <h2>Student Safety and Student Rights</h2>

                   <p class="text-justify ">

				A parent's primary concern is always a child's safety, yet parents routinely place their children's safety in the hands of the school for large portions of the day. Schools take this responsibility very seriously and enact codes of conduct designed to keep students safe and focused on school work. These codes of conduct often include bans on drugs and weapons on school property. Certain types of adult speech, such as obscenity, hate speech, and sexualized speech are considered disruptive to the functioning of the school, and are severely limited. As personal technology creeps further into students' lives and poses an ever increasing distraction, schools are also cracking down on the use of technology. Finally, schools are taking a more active role in protecting students against bullying in all its forms.</p>

<P class="text-justify ">However, a school can only protect students in ways that do not infringe on their constitutional rights. Students have fewer rights than adults, but they still have limited rights to speech and privacy that schools must respect. Furthermore, schools cannot automatically expel students for bad behavior; instead, they must provide the student with notice and a hearing before revoking education privileges. The section on "Student Rights" explains further.</p>

<P class="text-justify ">Finally, according to federal law, each student is entitled to a "free appropriate public education." This means that schools are required to make reasonable accommodations for students with disabilities. Students, parents, teachers, and doctors must collaborate and determine the best way to educate each child with special needs so that they can be adequately prepared for life after school. See "Special Education and Disabilities" for more information.</P>

                <h2>Education Law for Teachers</h2>
				   <p class="text-justify ">
            The law has measures designed to protect teachers' rights as employees, as well as teachers' academic freedoms. One major vehicle for protecting teacher rights are teachers' unions, which sets standards on how long a teacher can work and what duties they may undertake. The section on teachers' rights has more information on education law as it applies to teachers.
				    </p>

