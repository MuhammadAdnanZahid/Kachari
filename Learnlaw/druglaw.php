                   <p class="text-justify">Modern medical research has produced a wealth of medications that can improve health and extend both the length and quality of life. Some medications on the market pose dangers to consumers, however, and many people have suffered injuries as a result. Those who have been injured from dangerous drugs may bring products liability lawsuits against pharmaceutical companies that fail to test or manufacture drugs properly. Below you can find information about various dangerous drugs; how to file a lawsuit; information on hiring an attorney specializing in dangerous drugs product liability; as well as helpful forms and resources. Please select from the links below to get started.</p>

<h2>Toxic Chemicals & Materials</h2>
 <p class="text-justify">Toxic chemicals and materials have made their way into all types consumer goods, houses, buildings, work materials, and more. From lead paint on toys to chemicals used in the construction of our homes and workplaces, these toxic substances can give rise to wide ranging injuries. Injuries arising from toxic materials or chemicals can give rise to different types of legal actions, including standard product liability lawsuits as well as "toxic torts." Some toxic chemicals and materials have since been phased out of production in the U.S., including lead-based paints, DDT, and asbestos. However, even after manufacturers stop using a given substance, lawsuits over injuries arising from years of prior exposure can continue for some time. Here, you will find resources about toxic substances found in common products, in the home, on the job, and elsewhere.
                   </p>
                  
                   <h2>Defective Medical Devices</h2>

                    <p class="text-justify">
                    Dangerous or defective medical devices -- such as faulty surgical instruments, implants, pacemakers, and prosthetics -- can give rise to a product liability claim if a person who undergoes surgery or uses a medical device is injured or dies as a result. Although closely monitored by the U.S. Food and Drug Administration (FDA), a product can be defective in its design, manufacturing process, or marketing strategy. In most instances, state lawsuits over federally-approved medical devices face stringent legal obstacles and often receive increased national attention. Below, you will find resources that highlight common high-risk medical devices such as stents and defibrillators, and links to product liability lawyers in your area, as well as helpful forms and other resources. Please select from the links below to get started.</p>
                     <h2>Dangerous Baby & Kids' Products</h2>
                      <p  class="text-justify">Like other products liability claims, those relating to children's products can be made against manufacturers, suppliers, distributors, and retailers. This can include toys, children's equipment such as cribs, strollers, baby food, and other products sold for use by babies and children. Product liability claims can result in compensation for serious injuries, illness or deaths caused by products which are unduly dangerous either in their very design, through a manufacturing flaw, through unsafe handling along the path from manufacturer to the consumer, or through inadequate warnings of risks. FindLaw's "Dangerous Baby and Kids' Products" section provides in-depth resources pertaining to drop-side cribs, BB guns and other dangerous children's products.</p>

                      <h2>Why Drugs Get Pulled from the Market</h2>

                       <p class="text-justify">

                          A drug is removed from the market when its risks outweigh its benefits. This typically occurs when a safety hazard cannot be corrected, such as when it is discovered that the drug can cause serious side effects that were not known at the time of approval. However, completely removing a unique product from the market could be very dangerous to people who depend on the drug. So, decisions to remove products from the market are made very carefully, especially if people would be in danger without the product.
                                             </p>

                       <h2>How a Products Liability Attorney Can Help</h2>
                   <p class="text-justify">

                    If you or a loved one experienced any dangerous symptoms or unusual medical conditions while using pharmaceuticals, you should first contact your doctor or other healthcare professional. You may also want to meet with an experienced attorney to discuss your options and to protect your right to a legal remedy for any injuries. An experienced products liability attorney can help to guide you through the negotiation and settlement process. Your attorney can help you determine your damages, secure evidence of your losses, and advise you on different settlement options. To get started, at absolutely no charge, have an experienced products liability lawyer evaluate your claim today.
                                       </p>

                      
