                   <p class="text-justify">Employment law is the section of United States laws that determines how an employee and employer can work together. It regulates the relationship between workers, managers, and owners to ensure everyone is treated fairly and respectfully. It includes how and when an employee can work, what they should be paid, and the minimum conditions that are safe and appropriate to work in. It also determines when someone can be hired or fired and outlines the rights of employees and employers. </p>

 <p class="text-justify">Both federal and state governments have enacted a wide range of employment laws protecting employees from discriminatory treatment, unfair labor practices, unsafe work conditions, and more. This section provides in-depth resources on all phases of the employment process — from the interview and hiring stage to promotion and termination. In addition, you'll find information about privacy in the workplace, wage and hour laws, workplace safety, family leave policies, and detailed advice on hiring an employment lawyer.
                   </p>

                   <h2>Common Employment Law Issues</h2>

                    <p class="text-justify">
                      Employment law issues can arise in a wide range of situations. One of the most common circumstances occurs when an employee is fired for an illegal reason. Under federal law, employees may not be terminated on the basis of their race, gender, ethnicity, religion, disability or age. Employers who do so may be subject to civil liability for wrongful termination. The ban on discrimination applies not only to firings, but to other aspects of employment, including hiring and promotion decisions.
                   </p>
                    <p class="text-justify">
                      Federal and state laws also protect employees from unfair labor practices. Employers must comply with wage and hour laws, like the Fair Labor Standards Act, that dictate how long an employee can work and how much he or she must be paid. Employees' rights extend to family life as well. For example, the Family and Medical Leave Act ensures that employees are granted time off to welcome a new baby, or deal with illness.
                   </p>
                    <p class="text-justify">
                      Other employment law issues involve workplace conditions. The Occupational Safety and Health Administration, for example, requires employers to provide a safe workplace by adhering to national safety standards. Failing to do so can lead to fines and civil liability. In addition, employees generally have certain privacy rights while at work. For example, employers typically can't monitor personal telephone conversations or search an employee's car. Whether you are entering the job market for the first time or were recently terminated, it is important to understand your rights as a worker.                   </p>

                   <h2>Employment Law Attorneys</h2>

                   <p class="text-justify">

                    If you or a loved one is involved in an employment law dispute, it's in your best interests to consult with an attorney. Since employment is such a broad area of the law, employment lawyers typically specialize in one or a few aspects of practice. It's therefore important for you to seek out a lawyer who is experienced in the relevant area of employment law. For example, if you feel that you've been the victim of workplace discrimination, you should seek out an attorney who has experience handling the type of discrimination claim you're bringing. On the other hand, if you're looking to appeal a workers' compensation decision, you should find an attorney who is versed in the administrative procedures involved in workers' compensation law.

                   </p>


