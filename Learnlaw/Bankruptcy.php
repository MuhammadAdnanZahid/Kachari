                   <p class="text-justify">Those who have faced financial hardship know how difficult it is to deal with debt. Fortunately for many debtors, bankruptcy can provide a way out. Bankruptcy is designed to give debtors a financial fresh start by eliminating most debts in one fell swoop. However, it isn't a walk in the park. In order to receive a debt discharge, debtors are typically required to make major sacrifices, such as liquidating their assets, or forfeiting a percentage of their income. </p>

 <p class="text-justify">While there were a number of different federal bankruptcy laws in the past, the current Bankruptcy Code was enacted in 1978 and has been amended numerous times since. The Code provides several ways for debtors to eliminate their debts through bankruptcy. In this section, you'll find articles and resources on the different forms of bankruptcy, common issues that debtors face, getting legal help with bankruptcy, alternatives to filing, and more.
                   </p>

                   <h2>The Forms of Bankruptcy</h2>

                    <p class="text-justify">
                     There are six different types of bankruptcy in the U.S. Each form is designed for a specific purpose and has its own advantages and disadvantages. In a Chapter 7, for example, most of the debtor's property is sold off in order to repay creditors. In a Chapter 13, on the other hand, the debtor retains his or her property and instead pays off his or her debts according to a repayment plan. Finally, Chapter 11 bankruptcy is typically used by businesses who wish to shed debt and cut costs through reorganization. The type of bankruptcy you file depends on your assets, earning capacity, debt burden, and a number of other factors.
                   </p>
                    <h2>Common Issues</h2>
                    <p class="text-justify">
                      While bankruptcy is an important tool in resolving your debt problems, it does have its downsides. For example, bankruptcy can damage your credit for years to come. The cost of filing is also prohibitive for many people. In addition, a number of issues can arise during the bankruptcy process itself. For instance, failing to list all of your debts or assets in your filing can lead to a denial of your debt discharge. At times, debtors and creditors can also become embroiled in disputes over the repayment of debts.
                   </p>
                   <h2>Bankruptcy Attorneys</h2>
                    <p class="text-justify">
                      Whether you're an individual who is struggling with debt, or a business looking to reorganize, it's most often in your best interests to consult with an attorney. A bankruptcy lawyer can help you get the best possible terms and make the process go as smoothly as possible. If you're filing for a Chapter 7 bankruptcy, for example, a lawyer can help you take advantage of the many bankruptcy exemptions offered. If you're filing for Chapter 11, on the other hand, a bankruptcy attorney can help you develop a repayment plan that will ensure your business exits bankruptcy in a timely fashion.           </p>

                   <h2>Filing for Bankruptcy? Contact a Local Attorney</h2>

                   <p class="text-justify">

                    Since bankruptcy is such a broad area of the law, bankruptcy attorneys typically specialize in one or two areas of practice. For instance, some attorneys focus on Chapter 11 and corporate bankruptcy, while others specialize in personal bankruptcies. If you have questions about the process or need representation, consider working with a local bankruptcy attorney.
                   </p>


