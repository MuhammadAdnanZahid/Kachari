                   <p class="text-justify">People's tendency to get into accidents and suffer injuries is as old as civilization itself, as is the fact that sometimes the actions (or inactions) of others are to blame. While no one can go back and undo an injury, personal injury law provides a way for the injured to be "made whole" again. Relief for injuries usually comes in the form of monetary compensation, or "damages," but remedies may also include things other than money (such as a judge's order to take a particular action). Lawsuits for injuries often are settled out of court, before the court reaches a verdict. </p>

 <p class="text-justify">FindLaw's Accidents and Injuries section covers the broad legal practice area of injury law, which focuses on determining one or more party's liability for the injuries of another. Injury (or "personal injury") law addresses everything from car accidents and injuries caused by slippery shop floors, to dangerous consumer products and intentional acts.
                   </p>

                   <h2>What is an Injury?</h2>

                    <p class="text-justify">
                      An "injury" can be a lot of things, but is broadly defined as a violation of another's legally protected interest. This includes one's mental or physical well-being, their property, and even their reputation. Violation of any of these interests may entitle the injured party to seek relief in court. Here are some examples:
                      <li>A woman with an injured right foot goes into surgery to have it removed. The surgeon mistakenly amputates her left foot instead. The patient may sue the surgeon, the hospital, and any other responsible parties in a medical malpractice or negligence suit.</li>
                      <li>One man strikes another man with a baseball bat after a verbal disagreement. The first man may be sued for assault and/or battery in civil court, in addition to facing potential criminal penalties..</li>
                      <li>A woman in a small town writes an op-ed in the local newspaper that knowingly and falsely accuses her neighbor of being a drug dealer, which damages his reputation. He may sue her for defamation, since the publication of the false information injured his reputation.</li>
                   </p>
                    <h2>What are Damages?</h2>
                    <p class="text-justify">
                      The term damages refers to an award, usually a monetary one, for injuries. "Compensatory damages" are meant to compensate the plaintiff for his or her injuries, while "punitive damages" are reserved for especially egregious violations where the defendant may have acted with malice. Some states have limits on the amount of damages a plaintiff may collect, particularly with respect to medical malpractice claims.
                   </p>
                    <p class="text-justify">
                      Other employment law issues involve workplace conditions. The Occupational Safety and Health Administration, for example, requires employers to provide a safe workplace by adhering to national safety standards. Failing to do so can lead to fines and civil liability. In addition, employees generally have certain privacy rights while at work. For example, employers typically can't monitor personal telephone conversations or search an employee's car. Whether you are entering the job market for the first time or were recently terminated, it is important to understand your rights as a worker.                   </p>

                   <h2>What Can an Injury Attorney Do For Me?</h2>

                   <p class="text-justify">

                    If you have a legitimate claim for injury, a personal injury lawyer will be able to thoroughly prepare for your case and fight for your claim. Your attorney will know how to deal with insurance companies, analyze the evidence, bring in relevant witnesses and experts, negotiate a settlement, and generally apply the law to your benefit.
                   </p>


