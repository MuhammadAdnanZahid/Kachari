                   <p class="text-justify">Like most motorists, you will likely get a traffic ticket or be stopped by the police at least once in your lifetime. Minor traffic violations such as speeding or double parking generally are classified as "infractions" that are not punishable by jail time. Most of the time, fines for traffic tickets can be paid through the mail without the need for legal representation. But for more serious traffic offenses, or when extra points on your license could lead to a suspension, it makes sense to consult with an attorney.</p>

<p class="text-justify">FindLaw's Traffic Laws section covers everything from getting pulled over by police and having your vehicle searched, to understanding your rights and fighting a traffic ticket. This section also provides a state-by-state directory of traffic laws and DMV offices.</p>

<h2>A Brief History of Traffic Laws</h2>
 <p class="text-justify">Cars were mostly a novelty when they were first introduced at the turn of the 20th century, so they generally followed the rules established for horse-drawn wagons and carriages. But cars quickly became popular, and dangerous, prompting the state of New York to require owners to register their vehicles in 1901. Other states followed suit and by 1920, all states required vehicles to have license plates. Most states required driver's licenses by 1935, but only a few of them actually tested drivers.
                   </p>
                   <p class="text-justify">Many of the traffic regulations we take for granted, such as stop signs and crosswalks for pedestrians, were suggested in William Eno's 1903 book, "Rules of the Road." He also advocated for slow traffic to remain on the right side and for cars to pass on the left, the use of one-way streets, and the construction of safety islands between opposing lanes of traffic. Many of his ideas were gradually adopted as states developed their own traffic codes.
                   </p>

                   <h2>Common Traffic Law Violations</h2>

                    <p class="text-justify">
                    While each state has its own traffic code, there are only slight variations from one state to the next on the major rules of the road. For example, all states use the same or similar types of traffic signs. But the maximum speed limit on freeways differs substantially from state to state, with one section of freeway in Texas boasting an 85 miles per hour limit. Below are some examples of common traffic law violations:
                    <li>Speeding</li>
                    <li>Making an Illegal U-Turn</li>
                    <li>Running a Stop Sign</li>
                    <li>Failure to Wear Seat Belt</li>
                    <li>Broken Tail Light</li>
                    
                    <h2>Driver's License & Vehicle Info</h2>
                    <p class="text-justify">
                      So you have a brand new license and a brand new car, and you want to hit the road. Just make sure your car and your finances meet all the legal requirements before you back out of the driveway. Automobile insurance, emissions standards, vehicle registration, and the issuance of driver's licenses are regulated at the state level. Most states require that drivers maintain a minimum level of auto insurance so that medical and property bills do not go unpaid after an accident, and regulate emissions to keep the air clean. This section provides state-specific links related to these topics, frequently asked questions about vehicle inspections and more.
                   </p>
                   <h2>Vehicle Rules</h2>
                    <p class="text-justify">
                      Vehicles must also adhere to certain rules before they are allowed on the road. Failure to maintain a vehicle or unlawful modifications added to an otherwise functioning car can result in a violation that can result in tickets and fines. Standards vary depending on the state. Some states require safety or emissions inspections to ensure compliance with certain standards. </p>

                   <p class="text-justify">

                    Regardless of whether your state has inspection requirements certain features of your car must be in working condition when you operate it. Some examples include safety features such as seatbelts, turn signals, and headlights. Even if they work some parts of your car can still create liability.
                   </p>
                   <p class="text-justify">

                    Tinted windows or other cosmetic modifications can result in a ticket. Many states forbid tinting of certain kinds, tinting beyond a specified opacity, and in some cases apply different rules for the tinting of different windows. They may also forbid the installation of shades or other devices that obstruct visibility. Because vehicle restrictions can vary greatly from state-to-state it is best to check your state's specific requirements when buying or modifying a vehicle to ensure that you comply with all of the requirements for vehicle safety and maintenance in your area.
                   </p>

                   <h2>How an Attorney Can Help with a Traffic Violation</h2>
                      <p  class="text-justify">With many traffic violations, all you do is write a check and send it to your local traffic court. But sometimes it pays to hire a lawyer, depending on what's at stake. Even a $50 ticket can be worth fighting if the points accrued would result in a license suspension, for instance. And even minor traffic tickets with negligible fines can result in a higher insurance premium, so you want to consider the overall consequence of your violation</p>
                      <p   class="text-justify">Generally speaking, you should consider hiring a lawyer for serious traffic offenses such as leaving the scene of an accident; reckless driving; and of course drunk driving. An attorney will be able to determine whether it makes sense to fight a traffic charge or just pay the fine.</p>
                 
